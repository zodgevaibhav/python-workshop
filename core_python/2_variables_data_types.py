def learn_the_data_types():
    my_integer: int = 42
    my_float: float = 3.14159
    my_string: str = "Hello, World!"
    my_boolean: bool = True
    my_list: list = [1, 2, 3, 4, 5, 'cc']
    my_dict: dict = {"name": "John", "age": 30, "city": "New York"}
    my_tuple: tuple = (1, 2, 3, 4, 5, "cc")
    my_set: set = {1, 2, 3, 4, 5, "cc"}
    my_integer: float = 42.5

    print("Integer:", my_integer)
    print("Float:", my_float)
    print("String:", my_string)
    print("Boolean:", my_boolean)
    print("List:", my_list)
    print("Dictionary:", my_dict)
    print("Tuple:", my_tuple)
    print("Set:", my_set)


if __name__ == '__main__':
    learn_the_data_types()
